//Practical Number 05
//Areas covered Iteration control structure
//1.Using for loop
#include <stdio.h>
#include <stdlib.h>
int main()
{
 for(int no=0; no<=100; no++)
 {
 printf("%d\n",no);
 }
 return 0;
}

//1.Using do while
#include <stdio.h>
#include <stdlib.h>
int main()
{
 int no=0;
 do
 {
 printf("%d",no);
 no++;
 }
 while(no<=100);
 return 0;
}

//1.Using while loop
#include <stdio.h>
#include <stdlib.h>
int main()
{
 int no=0;
 while (no<=100)
 {
 printf("%d",no);
 no++;
 }
 return 0;
}








